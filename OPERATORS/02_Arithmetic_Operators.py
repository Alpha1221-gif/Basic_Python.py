# ----CODE01---- #
# Concept: Addition (+) - Adds two values together
x = 5
y = 3
print(x + y)
# output = 8

# ----CODE02---- #
# Concept: Subtraction (-) - Subtracts one value from another
x = 10
y = 4
print(x - y)
# output = 6

# ----CODE03---- #
# Concept: Multiplication (*) - Multiplies two values
x = 4
y = 3
print(x * y)
# output = 12

# ----CODE04---- #
# Concept: Division (/) - Divides one value by another (returns a float)
x = 10
y = 2
print(x / y)
# output = 5.0

# ----CODE05---- #
# Concept: Modulus (%) - Returns the division remainder
x = 7
y = 3
print(x % y)
# output = 1

# ----CODE06---- #
# Concept: Exponentiation (**) - Raises a number to the power of another
x = 2
y = 3
print(x ** y)
# output = 8

# ----CODE07---- #
# Concept: Floor division (//) - Divides and rounds down to the nearest whole number
x = 15
y = 4
print(x // y)
# output = 3
