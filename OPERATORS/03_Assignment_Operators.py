# ----CODE01---- #
# Concept: Assignment Operator (=) - Assigns a value to a variable
x = 5
print(x)
# output = 5

# ----CODE02---- #
# Concept: Addition Assignment (+=) - Adds a value and assigns the result
x = 5
x += 3
print(x)
# output = 8

# ----CODE03---- #
# Concept: Subtraction Assignment (-=) - Subtracts a value and assigns the result
x = 5
x -= 3
print(x)
# output = 2

# ----CODE04---- #
# Concept: Multiplication Assignment (*=) - Multiplies by a value and assigns the result
x = 5
x *= 3
print(x)
# output = 15

# ----CODE05---- #
# Concept: Division Assignment (/=) - Divides by a value and assigns the result
x = 5
x /= 3
print(x)
# output = 1.6666666666666667

# ----CODE06---- #
# Concept: Modulus Assignment (%=) - Takes modulus using a value and assigns the result
x = 5
x %= 3
print(x)
# output = 2

# ----CODE07---- #
# Concept: Floor Division Assignment (//=) - Takes floor division and assigns the result
x = 5
x //= 3
print(x)
# output = 1

# ----CODE08---- #
# Concept: Exponentiation Assignment (**=) - Raises to a power and assigns the result
x = 5
x **= 3
print(x)
# output = 125

# ----CODE09---- #
# Concept: Bitwise AND Assignment (&=) - Performs bitwise AND and assigns the result
x = 5  # Binary: 101
x &= 3 # Binary: 011
print(x)
# output = 1

# ----CODE10---- #
# Concept: Bitwise OR Assignment (|=) - Performs bitwise OR and assigns the result
x = 5  # Binary: 101
x |= 3 # Binary: 011
print(x)
# output = 7

# ----CODE11---- #
# Concept: Bitwise XOR Assignment (^=) - Performs bitwise XOR and assigns the result
x = 5  # Binary: 101
x ^= 3 # Binary: 011
print(x)
# output = 6

# ----CODE12---- #
# Concept: Bitwise Right Shift Assignment (>>=) - Shifts bits right and assigns the result
x = 5  # Binary: 101
x >>= 3
print(x)
# output = 0

# ----CODE13---- #
# Concept: Bitwise Left Shift Assignment (<<=) - Shifts bits left and assigns the result
x = 5  # Binary: 101
x <<= 3
print(x)
# output = 40

# ----CODE14---- #
# Concept: Walrus Operator (:=) - Assigns values to variables as part of an expression
print(x := 3)
# output = 3
