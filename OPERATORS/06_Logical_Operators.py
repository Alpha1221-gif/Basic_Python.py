# Concept: Logical AND (and) - Returns True if both statements are true
x = 4
print(x < 5 and x < 10)
# output = True

# ----CODE02---- #
# Concept: Logical OR (or) - Returns True if one of the statements is true
x = 4
print(x < 5 or x < 4)
# output = True

# ----CODE03---- #
# Concept: Logical NOT (not) - Reverses the result, returns False if the result is true
x = 4
print(not(x < 5 and x < 10))
# output = False
