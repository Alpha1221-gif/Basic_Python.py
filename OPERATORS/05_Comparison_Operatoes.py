# ----CODE01---- #
# Concept: Equal (==) - Checks if the values of two operands are equal
x = 5
y = 3
print(x == y)
# output = False

# ----CODE02---- #
# Concept: Not equal (!=) - Checks if the values of two operands are not equal
x = 5
y = 3
print(x != y)
# output = True

# ----CODE03---- #
# Concept: Greater than (>) - Checks if the left operand is greater than the right
x = 5
y = 3
print(x > y)
# output = True

# ----CODE04---- #
# Concept: Less than (<) - Checks if the left operand is less than the right
x = 5
y = 3
print(x < y)
# output = False

# ----CODE05---- #
# Concept: Greater than or equal to (>=) - Checks if the left operand is greater than or equal to the right
x = 5
y = 3
print(x >= y)
# output = True

# ----CODE06---- #
# Concept: Less than or equal to (<=) - Checks if the left operand is less than or equal to the right
x = 5
y = 3
print(x <= y)
# output = False
