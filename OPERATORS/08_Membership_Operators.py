# Concept: Membership Operator (in) - Returns True if a sequence with the specified value is present in the object
x = "banana"
y = ["apple", "banana"]

print(x in y)
# output = True


# ----CODE02---- #
# Concept: Membership Operator (not in) - Returns True if a sequence with the specified value is not present in the object
x = "pineapple"
y = ["apple", "banana"]

print(x not in y)
# output = True
