# ----CODE1----
# Concept: \' (Single Quote)
a = 'It\'s a beautiful moon'
print(a)
#output = It's a beautiful moon


# ----CODE2----
# Concept: \\ (Backslash)
b = "C:\\Users\\Moon\\Documents"
print(b)
#output = C:\Users\Moon\Documents


# ----CODE3----
# Concept: \n (New Line)
c = "Hello\nWorld"
print(c)
#output = Hello
#World


# ----CODE4----
# Concept: \r (Carriage Return)
d = "Hello\rWorld"
print(d)
#output = World


# ----CODE5----
# Concept: \t (Tab)
e = "Hello\tWorld"
print(e)
#output = Hello   World


# ----CODE6----
# Concept: \b (Backspace)
f = "Hello \bWorld"
print(f)
#output = HelloWorld


# ----CODE7----
# Concept: \f (Form Feed)
g = "Hello\fWorld"
print(g)
#output = Hello
#              World


# ----CODE8----
# Concept: \ooo (Octal value)
# \115 is 'M', \157 is 'o', \156 is 'n'
h = "The \115\157\157\156"
print(h)
#output = The Moon


# ----CODE9----
# Concept: \xhh (Hex value)
# \x4d is 'M', \x6f is 'o', \x6e is 'n'
i = "The \x4d\x6f\x6f\x6e"
print(i)
#output = The Moon
